package Model;

public class Xe_Model {
    private String maXe;
    private String tenXe;

    public Xe_Model(String maXe, String tenXe) {
        this.maXe = maXe;
        this.tenXe = tenXe;
    }

    public String getMaXe() {
        return maXe;
    }

    public void setMaXe(String maXe) {
        this.maXe = maXe;
    }

    public String getTenXe() {
        return tenXe;
    }

    public void setTenXe(String tenXe) {
        this.tenXe = tenXe;
    }
}
